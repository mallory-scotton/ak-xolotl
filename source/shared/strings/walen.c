///////////////////////////////////////////////////////////////////////////////
/// \brief Calculate the length of a null-terminated array of strings
///
/// \param wa   Pointer to the null-terminated array of strings
///
/// \return     The length of the array (number of strings)
///
///////////////////////////////////////////////////////////////////////////////
ulong my_walen(string *wa)
{
    string *swa = wa;

    for (; *wa; wa++);
    return (wa - swa);
}
