///////////////////////////////////////////////////////////////////////////////
/// \brief Set all characters in a string to null ('\0').
///
/// \param str   The string to clear.
///
/// \return      A pointer to the modified string.
///
///////////////////////////////////////////////////////////////////////////////
string my_strclr(string str)
{
    string save = str;

    for (; *str; ++str)
        *str = '\0';
    return (save);
}
